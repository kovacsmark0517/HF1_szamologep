namespace Szamolo_gep
{
    class Calculator
    {
        public static double Osszead(double egyikszam, double masikszam)
        {
            return egyikszam + masikszam;
        }

        public static double Kivon(double egyikszam, double masikszam)
        {
            return egyikszam - masikszam;
        }

        public static double Oszt(double egyikszam, double masikszam)
        {
            return egyikszam / masikszam;
        }

        public static double Szoroz(double egyikszam, double masikszam)
        {
            return egyikszam * masikszam;
        }

       
        
        public static double reszeredmeny()
        {
            return reszeredmeny();
        }
    }
